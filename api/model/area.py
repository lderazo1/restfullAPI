class Area:

    def __init__(self, id, name):
        self.id = id
        self.name = name

    # Setter methods
    def set_id(self, id):
        self.id = id

    def set_name(self, name):
        self.name = name

    # Getter methods
    def get_id(self):
        return self.id
    
    def get_name(self):
        return self.name
